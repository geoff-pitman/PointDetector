class Point:
    """
    A Point on a map

    @ivar x: Longitude point
    @ivar y: Latitude point
    """
    def __init__(self, x, y):
        """
        Initializes a point

        @param x: The x coordinate of the point
        @param y: The y coordinate of the point
        """
        self.x = x
        self.y = y
